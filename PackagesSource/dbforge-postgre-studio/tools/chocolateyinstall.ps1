$url        = 'https://www.devart.com/dbforge/postgresql/studio/dbforgepostgresql20.exe' 
$url64      = '' 
$packageArgs = @{
  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'exe' 
  url           = $url
  url64bit      = $url64
  softwareName  = 'dbForge Studio for PostgreSQL, v.2.0.111 Standard (Trial)' 
  checksum      = 'DDA320935E460F442DFC462E11FE04CE21ADD2DF4A957F3A832001307890529B'
  checksumType  = 'sha256' 
  silentArgs    = '/VERYSILENT'
  validExitCodes= @(0, 3010, 1641)
}
Install-ChocolateyPackage @packageArgs 